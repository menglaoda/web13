package com.deepblue.base.var;

public class CharDemo {

	public static void main(String[] args) {
		//char:字符型   表示单个字符。Java内部用整数表示
		char  e = 'D' ;
		System.out.println(e);
		e = '中' ;
		System.out.println(e) ;
		
		String str = "国庆70周年" ;
		System.out.println(str) ;
		
		//'' 和 "" 不能混淆
		// char c1 = "a" ; 类型不匹配
		
	}
	
}
