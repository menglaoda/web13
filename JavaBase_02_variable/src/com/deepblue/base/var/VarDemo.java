package com.deepblue.base.var;

public class VarDemo {

	public static void main(String[] args) {
		//常量
		System.out.println(1) ;
		int i = 0 ; //变量
		System.out.println(i);
		i = 6  ; //改变i的值为6
		System.out.println(i);
		
		
		System.out.println("美好生活，从Java开始！") ;//常量
		String str = "OK" ;
		System.out.println(str)  ;
		str = "Good" ;
		System.out.println(str)  ;
		
	}
	
}
